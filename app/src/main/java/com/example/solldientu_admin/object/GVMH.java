package com.example.solldientu_admin.object;

public class GVMH {
    private String maGv, maMh;

    public GVMH() {
    }

    public GVMH(String maGv, String maMh) {
        this.maGv = maGv;
        this.maMh = maMh;
    }

    public String getMaGv() {
        return maGv;
    }

    public void setMaGv(String maGv) {
        this.maGv = maGv;
    }

    public String getMaMh() {
        return maMh;
    }

    public void setMaMh(String maMh) {
        this.maMh = maMh;
    }
}
